
public class BooleanApp {

	public static void main(String[] args) {
		System.out.println("One");
		System.out.println(1);
		
		System.out.println(true);
		System.out.println(false);
		
		String foo = "Hello World";
		
		// public boolean contains(CharSequence s) string에서 사용할 수 있는 매서드, ()있으면 t 없으면 f
		System.out.println(foo.contains("World")); // 노란색 메모지 클릭하면 사용법 확인 가능
		System.out.println(foo.contains("Hyunji"));
	}

}
