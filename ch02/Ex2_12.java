
public class Ex2_12 {
	public static void main(String[] args) {
		String str = "3";
		
		System.out.println('3' - '0');
		//문자 '3'에서 '0'을 빼면, 숫자 3
		System.out.println('3' - '0' +1);
		System.out.println(3+1);
		System.out.println("3"+"1");
		//문자열3과 문자열1의 결합
		System.out.println(3 + '0');
		//숫자3에 '0'을 더하면 문자 '3', 문자0은 숫자 48
		System.out.println((char)(3+'0'));
		//숫자3을 문자화
		System.out.println(str);
	}
}
